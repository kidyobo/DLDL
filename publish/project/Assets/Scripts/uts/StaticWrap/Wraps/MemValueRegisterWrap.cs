		public static IntPtr GetType_S(IntPtr context, IntPtr function, IntPtr thisObj, int argcnt, IntPtr args, IntPtr exception) {
			try {
				Type csType = typeof(MemValueRegister);
				return MakeJsObjects.MakeJsObject(context, csType);
			} catch (Exception e) { return Native.jvm_throwerr(context, exception, e.ToString()); }
		}
		
		static public void Register(IntPtr env) {
			Native.jvm_reg_class_start(env);
			Native.jvm_reg_static_function(env, "GetString", GetString_S);
			Native.jvm_reg_static_function(env, "RegString", RegString_S);
			Native.jvm_reg_static_function(env, "GetInt", GetInt_S);
			Native.jvm_reg_static_function(env, "RegInt", RegInt_S);
			Native.jvm_reg_static_function(env, "GetBool", GetBool_S);
			Native.jvm_reg_static_function(env, "RegBool", RegBool_S);
			Native.jvm_reg_static_function(env, "GetType", GetType_S);
			jsClass = Native.jvm_reg_class_end(env, "MemValueRegister", IntPtr.Zero, ctor, ObjectsMgr.dtor);
			RegHelper.RegisterJsClass(typeof(MemValueRegister), jsClass);
		}
	}
}
